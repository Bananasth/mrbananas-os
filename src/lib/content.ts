export function getArticle(slug: string) {
  return articles.find((a) => a.slug === slug);
}

/** รวม FAQ ทั้งหมด (หน้าหลัก + ในบทความ) สำหรับสร้าง FAQ schema/llms.txt */
export function allFaqs(): Faq[] {
  const fromArticles = articles.flatMap((a) => a.faqs ?? []);
  return [...faqs, ...fromArticles];
}
